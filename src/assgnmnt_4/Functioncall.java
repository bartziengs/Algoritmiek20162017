package assgnmnt_4;

public class Functioncall {
	
protected int from;
protected int to;
protected int amount;

public Functioncall(int from, int to, int amount){
	this.from = from;
	this.to= to;
	this.amount = amount;
}

public int getFrom() {
	return from;
}

public int getTo() {
	return to;
}

public int getAmount() {
	return amount;
}





}
